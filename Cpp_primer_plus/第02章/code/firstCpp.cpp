#include <iostream> // 导入头文件

int main()                                   // 函数头
{                                            // 函数主题
  using namespace std;                       // 使用std的命名空间中的函数
  cout << "Come up and C++  me some time ."; // 输出，;是必须的，不能省略
  cout << endl;                              // 换行
  cout << "You won't regret it!" << endl;    // 输出
  return 0;                                  // main 函数终止
}                                            // 函数主体模块
